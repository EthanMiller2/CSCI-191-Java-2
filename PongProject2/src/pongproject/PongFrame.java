    public void launchBall() {
        int ballx = fieldWidth / 2;
        int bally = fieldHeight / 2;
        int ballxv = randoms.nextInt(16) + 5;
        int other = randoms.nextInt(2);
        if (other % 2 == 0) {
            ballxv = ballxv;
        } else {
            ballxv = -ballxv;
        }
        int ballyv = randoms.nextInt(31) - 15;
        balls.add(new Ball(ballx, bally, ballxv, ballyv, 5, Color.red));
    }


    public static final int paddleHeight = 50;
    public static final int fieldWidth = 600;
    public static final int fieldHeight = 300;
    public static final int leftPaddlex = 15;
    public static final int rightPaddlex = fieldWidth - 15;
    public Random randoms = new Random();
    public ArrayList<Ball> balls = new ArrayList<Ball>();
    public Paddle left = new Paddle(leftPaddlex, paddleHeight, paddleWidth, 5);
    public Paddle right = new Paddle(rightPaddlex, paddleHeight, paddleWidth, 10);

    public Timer clock = new Timer(50, new ActionListener() {  // 50ms delay between ticks

        public void actionPerformed(ActionEvent e) {
            tick();               // Write a method named tick to advance your game
            ViewPanel.repaint();

        }

        public void tick() {

            tickcount++;
            if (tickcount % 20 == 0) {
                launchBall();
            }
            for (Ball b : balls) {
                b.move();
                right.Bounce(b);
                left.Bounce(b);
            }

            ArrayList<Ball> nb = new ArrayList<Ball>();
            for (Ball b : balls) {
                if (!b.score) {
                    nb.add(b);
                }
            }
            balls = nb;

            right.sety(mousey);

            int min = 800;
            int t = 300;
            for (Ball b : balls) {
                if (b.xv < 0 && b.x < min) {
                    min = b.x;
                    t = b.y;
                }
            }
            left.sety(t);

            RightScore.setText("Right Score: " + left.score);
            LeftScore.setText("Left Score: " + right.score);

        }
    });

